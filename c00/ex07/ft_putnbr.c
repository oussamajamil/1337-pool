#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	mod;
	char	a;

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = nb * (-1);
		write (1, "-", 1);
	}
	if (nb < 10 )
	{
		a = nb + 48;
		write (1, &a, 1);
	}
	else
	{
		ft_putnbr (nb / 10);
		mod = nb % 10 + 48;
		write (1, &mod, 1);
	}
}
